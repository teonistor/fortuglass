/* Draw shape according to specified structure parameters and based on bit patterns above */
void paint_shape (struct shape *shape, uint16_t color) {
	write_cmd(PAGE_ADDRESS_SET);
    write_data16(shape -> y - shape -> rad);
    write_data16(shape -> y + shape -> rad);
	write_cmd(COLUMN_ADDRESS_SET);
    write_data16(shape -> x - shape -> rad);
    write_data16(shape -> x + shape -> rad);
	write_cmd(MEMORY_WRITE);

	uint8_t index, max_index;
	int32_t mask, *pattern;

	switch (shape -> kind) {
		case prism_kind:
			max_index = PRISM_RADIUS * (PRISM_RADIUS + 1) / 8;
			pattern = prism16 [shape->rot];
			break;
		case mirror_kind:
			max_index = MIRROR_RADIUS * (MIRROR_RADIUS + 1) / 8;
			pattern = mirror24 [shape->rot];
			break;
		case target_kind:
			max_index = TARGET_RADIUS * (TARGET_RADIUS + 1) / 8;
			pattern = target1;
			break;
		case sun_kind:
			max_index = SUN_RADIUS * (SUN_RADIUS + 1) / 8;
			pattern = sun1;
			break;
	}

	for (index = 0, mask = 1; index <= max_index;) {
		write_data16((pattern[index] & mask) ? color : display.background);
		mask <<= 1;

		if (!mask) {
			mask = 1;
			index ++;
		}
	}
}

